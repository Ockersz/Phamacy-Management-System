    private void btn_delActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_delActionPerformed
        // delete some item

        DefaultTableModel dt = (DefaultTableModel) tbl_cart.getModel();

        
        if(tbl_cart.getSelectionModel().isSelectionEmpty())
        {
            JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(),"Please select an item to delete","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        String r = dt.getValueAt(tbl_cart.getSelectedRow(), 0).toString();
        CCart.deleteItem(Integer.parseInt(r));
        
        int rw = tbl_cart.getSelectedRow();
        dt.removeRow(rw); 
        
        refreshCart();
        cal(); // after iteam delete calculate total
        }
    }//GEN-LAST:event_btn_delActionPerformed

