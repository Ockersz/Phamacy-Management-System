    public void refreshCart()
    {
        ResultSet rs = (ResultSet) CCart.loadCart();
        DefaultTableModel dtm = new DefaultTableModel(); 
        
        tbl_cart.getTableHeader().setReorderingAllowed(false);
        dtm.addColumn("Id");
        dtm.addColumn("Name");
        dtm.addColumn("Quantity");
        dtm.addColumn("Sub Total");
        
        
        try{
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(),"Cart is empty, please add products first");
            }
            else{
            do
            {
                String[] values = new String[4];
                values[0] = Integer.toString(rs.getInt(1));
                values[1] = rs.getString(2);
                values[2] = Integer.toString(rs.getInt(3));
                values[3] = Integer.toString(rs.getInt(4));
                
                dtm.addRow(values);
            }
            while(rs.next());
            tbl_cart.setModel(dtm);
            tbl_cart.getColumnModel().getColumn(1).setPreferredWidth(150);
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(),e.getMessage());
        }
    }
    
