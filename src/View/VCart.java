    private void btn_payActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_payActionPerformed
        // pay btn
        if(txt_cashpay.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Input cash amount to pay");
        }
               
        else if(!txt_cashpay.getText().matches("\\d+"))
        {
            JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Cash amount cannot contain letters");
        }
                
        else{
            if(tbl_cart.getModel().getRowCount()==0)
            {
                JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Your cart does not have any products to make payment","Error",JOptionPane.ERROR_MESSAGE);
            }
            
            
            else{
            int tot = Integer.parseInt(txt_tot.getText());
            int paid = Integer.parseInt(txt_cashpay.getText());
            int balance =0;
            
            if(tot<paid)
            {
             balance = paid - tot ;
            
           // DecimalFormat df = new DecimalFormat("00.00") ;
        
            txt_balance.setText(String.valueOf(balance));
            
            COrder.makeOrder(Integer.parseInt(txt_tot.getText()), VLogin.emp_id);
            JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Order made Successfully","Info",JOptionPane.INFORMATION_MESSAGE);
            
            showReceipt();
            CCart.emptyCart();
            DefaultTableModel dt = (DefaultTableModel) tbl_cart.getModel();
            dt.setRowCount(0);
            
            }
            else
            {
                JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "The amount given is not sufficient");
            }
           
            }
        }
    }//GEN-LAST:event_btn_payActionPerformed

