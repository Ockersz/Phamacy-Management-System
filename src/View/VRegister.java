    private void btn_registerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_registerActionPerformed
        // TODO add your handling code here:
        String uname = CLogin.checkUsernameExist(txt_username.getText());
        String textname = txt_username.getText();
                textname = textname.toLowerCase();
        if(txt_fname.getText().length() == 0 )
             txt_error.setText("*First name cannot be blank");
        else if(!txt_fname.getText().matches("^[a-zA-Z]+$"))
            txt_error.setText("*First name cannot contain numbers");
        else if(txt_lname.getText().length() == 0)
            txt_error.setText("*Last name cannot be blank");
        else if(!txt_lname.getText().matches("^[a-zA-Z]+$"))
            txt_error.setText("*Last name cannot contain numbers");
        else if(txt_homeno.getText().length() == 0)
            txt_error.setText("*Home number cannot be blank"); 
        else if(txt_street.getText().length() == 0)
            txt_error.setText("*Street name cannot be blank");
        else if (txt_city.getText().length() == 0)
            txt_error.setText("*City name cannot be blank");
        else if(!txt_city.getText().matches("^[a-zA-Z]+$"))
            txt_error.setText("*City name cannot contain numbers");
        else if (txt_email.getText().length()== 0)
            txt_error.setText("*Email address cannot be blank");
        else if (!txt_email.getText().matches("^[a-zA-Z0-9_!#$%&â€™*+/=?`{|}~^.-]+@[a-zA-Z0-9.-]+$"))
            txt_error.setText("*Email not in correct format eg: someone@example.com");
        else if (txt_tp.getText().length() == 0 )
            txt_error.setText("*Telephone number cannot be blank");
        else if (!txt_tp.getText().matches("^(?:7|0|(?:\\+94))[0-9]{9,10}$"))
            txt_error.setText("*Telephone number not in correct format eg: 0712345678");
        else if (txt_username.getText().length() == 0)
            txt_error.setText("*Username cannot be blank");
        else if (textname.equals(uname))
            txt_error.setText("*Username Exists try another");
        else if (txt_pwd.getText().length()==0)
            txt_error.setText("*Password cannot be blank");
        else if (!Arrays.equals(txt_pwd.getPassword(), txt_cnfpwd.getPassword()))
            txt_error.setText("*Password mismatch"); 
        else{
            txt_error.setText("");
            String district = cmb_district.getSelectedItem().toString();
            String province = cmb_province.getSelectedItem().toString();
            Date date = dtp_dob.getDate();
            String gender = "";
            if(rbtn_male.isSelected())
            {
                gender = "male";
            }
            else if(rbtn_female.isSelected())
            {
                gender = "female";        
            }
            
            int i =  CLogin.registerEmployee(txt_fname.getText(),txt_lname.getText(),gender,txt_homeno.getText(),txt_street.getText(),txt_city.getText(), district, province,txt_email.getText(),date ,txt_tp.getText(),txt_username.getText(),txt_pwd.getText());
            if (i==1)
            {
                JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Successfully registered" , "Info" , JOptionPane.INFORMATION_MESSAGE);
                VLogin obj = new VLogin();
                obj.setVisible(true);
                this.dispose();
            }
            else
            {
                 JOptionPane.showMessageDialog(CMessageManagement.getVisibleContent(), "Registration unsuccessful" , "Error" , JOptionPane.ERROR_MESSAGE);
            }
            //fname, lname, gender, homeno, street, city, district, province, email, dob, tp
            
            
            
            
            
        
        }
    }//GEN-LAST:event_btn_registerActionPerformed

