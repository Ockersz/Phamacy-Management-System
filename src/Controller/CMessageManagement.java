package Controller;

import java.awt.Window;

/**
 *
 * @author Ockersz
 */
public class CMessageManagement {
    
    public static Window getVisibleContent() {

        Window result = null;
        for (Window w : Window.getWindows()) {
            if (w.isVisible()) {
                result = w;
            }
        }
        return result;
    }
}
